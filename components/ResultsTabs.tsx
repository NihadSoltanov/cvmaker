// PdfButton â€” generates PDF from letter content
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
function PdfButton({ title, content, name }: { title: string; content: string; name?: string }) {
    const [loading, setLoading] = useState(false);
    const handle = async () => {
        if (!content) return;
        setLoading(true);
        try {
            const { pdf, Document, Page, Text, View, StyleSheet } = await import("@react-pdf/renderer");
            const styles = StyleSheet.create({
                page: { fontFamily: "Times-Roman", fontSize: 11, color: "#111", padding: "52 58", lineHeight: 1.7, backgroundColor: "#fff" },
                heading: { fontSize: 16, fontFamily: "Times-Bold", marginBottom: 4 },
                date: { fontSize: 9.5, color: "#888", marginBottom: 22 },
                title: { fontSize: 10, fontFamily: "Times-Bold", textTransform: "uppercase", letterSpacing: 1.5, color: "#4338ca", borderBottom: "1pt solid #c7d2fe", paddingBottom: 4, marginBottom: 14 },
                para: { fontSize: 11, lineHeight: 1.72, color: "#1a1a1a", marginBottom: 12 },
                footer: { position: "absolute", bottom: 28, left: 58, right: 58, fontSize: 8.5, color: "#bbb", textAlign: "center", borderTop: "0.5pt solid #eee", paddingTop: 5 },
            });
            const date = new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" });
            const doc = (
                <Document title={title}>
                    <Page size="A4" style={styles.page}>
                        {name && <Text style={styles.heading}>{name}</Text>}
                        <Text style={styles.date}>{date}</Text>
                        <Text style={styles.title}>{title}</Text>
                        {content.split("\n\n").filter(Boolean).map((p, i) => (
                            <Text key={i} style={styles.para}>{p.trim()}</Text>
                        ))}
                        <Text style={styles.footer}>Generated by AI CV Optimizer Â· {date}</Text>
                    </Page>
                </Document>
            );
            const blob = await pdf(doc as any).toBlob();
            const url = URL.createObjectURL(blob);
            const a = document.createElement("a");
            a.href = url;
            a.download = `${title.replace(/\s+/g, "_")}.pdf`;
            document.body.appendChild(a); a.click(); document.body.removeChild(a);
            setTimeout(() => URL.revokeObjectURL(url), 1000);
        } finally { setLoading(false); }
    };
    return (
        <button onClick={handle} disabled={loading || !content}
            className="flex items-center gap-1.5 text-sm font-bold bg-indigo-600 text-white hover:bg-indigo-700 transition px-3 py-1.5 rounded-xl disabled:opacity-50">
            <Download className="w-4 h-4" />
            {loading ? "Generatingâ€¦" : "PDF"}
        </button>
    );
}

