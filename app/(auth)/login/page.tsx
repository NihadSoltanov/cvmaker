"use client";

import Link from "next/link";
import { ArrowLeft } from "lucide-react";
import { useRouter } from "next/navigation";
import { useState } from "react";
import { supabase } from "@/lib/supabase";

export default function LoginPage() {
    const router = useRouter();
    const [isLoading, setIsLoading] = useState(false);

    const [email, setEmail] = useState("");
    const [password, setPassword] = useState("");
    const [errorMsg, setErrorMsg] = useState("");

    const handleSubmit = async (e: React.FormEvent) => {
        e.preventDefault();
        setIsLoading(true);
        setErrorMsg("");

        const { data, error } = await supabase.auth.signInWithPassword({
            email,
            password,
        });

        if (error) {
            setErrorMsg(error.message);
            setIsLoading(false);
            return;
        }

        router.push("/dashboard");
    };

    const handleGoogleAuth = async () => {
        await supabase.auth.signInWithOAuth({
            provider: 'google',
            options: { redirectTo: `${window.location.origin}/dashboard` }
        });
    };
    return (
        <div className="min-h-screen flex items-center justify-center p-4 relative overflow-hidden">
            <div className="absolute top-1/4 right-1/4 w-[50%] h-[50%] bg-indigo-500/10 rounded-full blur-[100px] pointer-events-none" />

            <div className="glass-card w-full max-w-md p-8 rounded-3xl z-10">
                <Link href="/" className="inline-flex items-center text-sm font-medium text-neutral-500 hover:text-indigo-500 mb-8 transition">
                    <ArrowLeft className="w-4 h-4 mr-2" /> Back home
                </Link>

                <h1 className="text-2xl font-bold mb-2">Welcome back</h1>
                <p className="text-neutral-500 text-sm mb-8">Log in to manage your tailored applications.</p>

                <form className="space-y-4" onSubmit={handleSubmit}>
                    {errorMsg && <div className="p-3 bg-red-500/10 border border-red-500/20 text-red-500 rounded-lg text-sm">{errorMsg}</div>}
                    <div>
                        <label className="block text-sm font-medium mb-1">Email</label>
                        <input type="email" value={email} onChange={(e) => setEmail(e.target.value)} placeholder="you@example.com" className="input-styled" required />
                    </div>
                    <div>
                        <label className="block text-sm font-medium mb-1">Password</label>
                        <input type="password" value={password} onChange={(e) => setPassword(e.target.value)} placeholder="••••••••" className="input-styled" required />
                    </div>
                    <button type="submit" disabled={isLoading} className="btn-primary w-full mt-2">
                        {isLoading ? "Logging in..." : "Log In"}
                    </button>
                </form>

                <div className="my-6 flex items-center gap-4">
                    <div className="h-px bg-neutral-200 dark:bg-neutral-800 flex-1" />
                    <span className="text-xs text-neutral-500 uppercase font-semibold">Or</span>
                    <div className="h-px bg-neutral-200 dark:bg-neutral-800 flex-1" />
                </div>

                <button onClick={handleGoogleAuth} className="w-full flex items-center justify-center gap-2 border border-neutral-200 dark:border-neutral-800 rounded-md h-10 px-4 text-sm font-medium hover:bg-neutral-50 dark:hover:bg-neutral-900 transition">
                    <svg className="w-5 h-5" viewBox="0 0 24 24"><path fill="currentColor" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" /><path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" /><path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" /><path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" /></svg>
                    Continue with Google
                </button>

                <p className="mt-8 text-center text-sm text-neutral-500">
                    Don't have an account? <Link href="/signup" className="text-indigo-600 hover:underline">Sign up</Link>
                </p>
            </div>
        </div>
    );
}
